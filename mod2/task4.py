#Для введенного в десятичном коде натурального числа найти представление в двоичном, восьмеричном и шестнадцатеричном кодах. Если введено не натуральное число, 
#вывести диагностику: «Неверный ввод». Использовать встроенные возможности языка python запрещено.
n = input()

ok = True
# Проверка на пустую строку
if n == "":
    ok = False
else:
    # Проверка, что в строке только цифры 
    i = 0
    while i < len(n):
        if n[i] < '0' or n[i] > '9':
            ok = False
            break
        i += 1

# Если ввод некорректен или число не положительное
if not ok:
    print("Неверный ввод")
else:
    num = int(n)
    if num <= 0:
        print("Неверный ввод")
    else:
        # --- Перевод в двоичную систему (b2) ---
        x = num
        b2 = ""
        while x > 0:
            b2 = str(x % 2) + b2  # Берем остаток от деления на 2 и ставим в начало строки
            x = x // 2            # Делим число нацело на 2

        # --- Перевод в восьмеричную систему (b8) ---
        x = num
        b8 = ""
        while x > 0:
            b8 = str(x % 8) + b8  # Аналогично, остаток от деления на 8
            x = x // 8

        # --- Перевод в шестнадцатеричную систему (b16) ---
        x = num
        b16 = ""
        digits = "0123456789ABCDEF" # Справочник символов для остатков > 9
        while x > 0:
            r = x % 16
            b16 = digits[r] + b16  # Берем нужный символ из строки digits по индексу остатка
            x = x // 16

        # Вывод результатов через запятую
        print(f"{b2}, {b8}, {b16}")
