#На вход подается доменное имя сайта. Необходимо вывести все домены по порядку начиная с домена первого уровня.
s = input() # Считываем входную строку (например, "one.two.three")

parts = []  # Список для хранения фрагментов строки
cur = ""    # Временная переменная для накопления текущего слова
i = 0

# --- Блок разделения строки по точкам ---
while i < len(s):
    if s[i] == '.':
        # Если встретили точку, добавляем накопленное слово в список
        parts.append(cur)
        cur = "" # Очищаем временную переменную для следующего слова
    else:
        # Если это не точка, приклеиваем символ к текущему слову
        cur += s[i]
    i += 1

# Добавляем последнее слово (так как за ним точки нет, оно не попало в список в цикле)
parts.append(cur)

# --- Блок вывода слов в обратном порядке ---
i = len(parts) - 1 # Начинаем с индекса последнего элемента
while i >= 0:
    print(parts[i]) # Печатаем слово
    i -= 1          # Двигаемся к началу списка
