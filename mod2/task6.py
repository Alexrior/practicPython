#Строка состоит из 0 и 1. Выведите ‘yes’, если количество единиц совпадает с количеством нулей. И ‘no’ в противном случае.
s = input() # Считываем строку от пользователя

zeros = 0   # Счетчик для нулей
ones = 0    # Счетчик для единиц

i = 0       # Индекс для обхода строки
# Проходим циклом по каждому символу в строке
while i < len(s):
    if s[i] == '0':
        zeros += 1  # Если нашли '0', увеличиваем счетчик нулей
    elif s[i] == '1':
        ones += 1   # Если нашли '1', увеличиваем счетчик единиц
    i += 1          # Переходим к следующему символу

# Сравниваем итоговые значения счетчиков
if zeros == ones:
    print("yes")    # Выводим "yes", если нулей и единиц поровну
else:
    print("no")     # В противном случае выводим "no"
