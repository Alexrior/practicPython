#На вход подается строка s и символ i. Необходимо найти количество символов i, расположенных в начале строки.
string = input() # Ожидается ввод вида "строка,символ" (например, "aaab,a")

# Находим позицию первой запятой в строке
replacer = string.find(",")

# Отрезаем часть ДО запятой — это наша рабочая строка
s = string[:replacer]

# Берем один символ СРАЗУ ПОСЛЕ запятой
i = string[replacer + 1]

count = 0  # Счетчик совпадений
index = 0  # Индекс для обхода строки s с самого начала

# Пока символ в строке s совпадает с искомым символом i
# ВАЖНО: цикл прервется, как только встретится другой символ или конец строки
while index < len(s) and s[index] == i:
    count += 1    # Увеличиваем счетчик
    index += 1    # Переходим к следующему символу

# Выводим количество идущих подряд целевых символов в начале строки
print(count)
