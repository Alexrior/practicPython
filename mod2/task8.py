#Дан список слов. Составить из последних букв каждого слова новое.
s = input()  # Считываем строку от пользователя (например, "привет мир")

word = ""    # Переменная для накопления текущего слова
res = ""     # Результирующая строка из последних букв
i = 0

# Проходим по всей строке посимвольно
while i < len(s):
    if s[i] == ' ':
        # Если встретили пробел и перед ним было слово
        if word != "":
            # Берем последний символ накопленного слова и добавляем в результат
            res += word[len(word) - 1]
            word = "" # Очищаем переменную для следующего слова
    else:
        # Если это не пробел, добавляем символ в текущее слово
        word += s[i]
    i += 1

# После цикла проверяем, не осталось ли в переменной word последнего слова
# (так как в конце строки пробела обычно нет)
if word != "":
    res += word[len(word) - 1]

# Выводим строку, состоящую только из последних букв слов
print(res)
