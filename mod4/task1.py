#На вход подается список чисел через пробел. Напишите функцию выводящую сообщение для списка чисел:
#1) Все числа равны
#2) Все числа разные
#3) Есть равные и неравные числа

# Определяем функцию для анализа списка чисел
def analyze_numbers(nums):
    # Если размер множества равен 1, значит, все элементы в списке были одинаковыми
    if len(set(nums)) == 1:
        return "Все числа равны"
    
    # Если размер множества совпадает с длиной исходного списка,
    # значит, ни одно число не повторилось (дубликатов нет)
    elif len(set(nums)) == len(nums):
        return "Все числа разные"
    
    # Во всех остальных случаях (когда размер множества меньше списка, но больше 1)
    else:
        return "Есть равные и неравные числа"

# Считываем строку, разбиваем её по пробелам, превращаем в числа и кладём в список
numbers = list(map(int, input().split()))

# Вызываем функцию и печатаем результат
print(analyze_numbers(numbers))
