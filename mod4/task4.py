#На вход подается строка. Напишите функцию для реализации: если из слова можно составить палиндром, то составить его и вывести на экран


def make_palindrome(s):
    # 1. Считаем, сколько раз каждый символ встречается в строке
    counts = {}
    for ch in s:
        counts[ch] = counts.get(ch, 0) + 1

    # 2. Ищем символы, которые встречаются нечетное количество раз
    odd_chars = [ch for ch in counts if counts[ch] % 2 == 1]
    
    # В палиндроме может быть максимум ОДИН символ с нечетным количеством (в центре)
    if len(odd_chars) > 1:
        return "Нельзя составить палиндром"

    left = []
    middle = ""

    # 3. Формируем левую половину палиндрома
    # Сортировка sorted(counts) нужна, чтобы результат был первым по алфавиту
    for ch in sorted(counts):
        # Берем ровно половину от общего количества текущего символа
        left.append(ch * (counts[ch] // 2))
        # Если символа нечетное количество, он пойдет в самый центр
        if counts[ch] % 2 == 1:
            middle = ch

    # Собираем левую часть в одну строку
    left_part = "".join(left)
    
    # 4. Итоговый палиндром: левая часть + центр + перевернутая левая часть
    return left_part + middle + left_part[::-1]

# Считываем входное слово
word = input()
# Выводим результат работы функции
print(make_palindrome(word))
