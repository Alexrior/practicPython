#Создать класс. Стек на структуре данных односвязный список
class Node:
    """
    Вспомогательный класс для узлов списка (одиночный элемент стека)
    """
    def __init__(self, data):
        self.data = data  # Полезная нагрузка (само значение, например, число или строка)
        self.pref = None  # Ссылка на узел, который находится «под» текущим (предыдущий)


class Stack:
    """
    Основной класс для реализации стека (структура LIFO)
    """

    def __init__(self):
        self.end = None  # «Верхушка» стека. Изначально стек пуст, поэтому None.

    def pop(self):
        """
        Удаление верхнего элемента и возврат его значения
        """
        if self.end is None:  # Проверка: если стек пуст, удалять нечего
            return None
        
        val = self.end.data       # Запоминаем данные с текущей верхушки
        self.end = self.end.pref  # Переносим указатель верхушки на элемент ниже
        return val                # Возвращаем «снятое» значение

    def push(self, val):
        """
        Добавление нового элемента val на вершину стека
        """
        new_node = Node(val)      # Создаем новый узел с данными
        new_node.pref = self.end  # Указываем, что «под» новым узлом будет текущая верхушка
        self.end = new_node       # Делаем новый узел новой верхушкой стека

    def print(self):
        """
        Вывод всех элементов стека от верхушки к основанию
        """
        cur = self.end            # Начинаем просмотр с самого верхнего элемента
        while cur is not None:    # Пока не дошли до «дна» (где pref равен None)
            print(cur.data, end=' ')
            cur = cur.pref        # Переходим к следующему узлу вглубь стека
        print()                   # Перенос строки после вывода всего стека


# Создаем объект стека
s = Stack()

# Добавляем элементы (они ложатся друг на друга)
s.push(10)      # В стеке: [10]
s.push(20)      # В стеке: [20, 10]
s.push(30)      # В стеке: [30, 20, 10]
s.push('AAA')   # В стеке: ['AAA', 30, 20, 10] — 'AAA' теперь на самой вершине

# Печатаем текущее состояние: 'AAA' 30 20 10
s.print()

# Извлекаем верхний элемент ('AAA') и выводим его на экран
print(s.pop())

# Печатаем остаток стека: 30 20 10
s.print()
