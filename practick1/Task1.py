#Дано количество имён N. Вводится N имен и помещаются в список. Создайте новый список uni:
#- Пройдите по исходному списку names и добавьте имя в uni только в том случае, если имени с такой длиной в uni ещё нет. В итоге в uni должны быть имена разной длины.
#- Выведите оба списка.
def read_names(N):#Функция для чтения N имен из стандартного ввода.
    names = []
    for _ in range(N):
        name = input()  # Считываем строку
        # Проверяем, что строка не пустая (strip удаляет пробелы по краям)
        if name.strip():
            names.append(name)
    return names


if __name__ == "__main__":
    # Считываем количество ожидаемых имен
    N = int(input())
    
    # Вызываем функцию для заполнения списка имен
    names = read_names(N)
    
    # Список для хранения имен с уникальной длиной
    uni = []
    
    for name in names:
        # Если список uni еще пуст, добавляем первое имя без проверок
        if not uni:
            uni.append(name)
            continue
        
        # Проверяем, есть ли уже в списке uni имя с такой же длиной, как у текущего
        # [len(u) for u in uni] — создает временный список длин всех имен в uni
        if len(name) not in [len(u) for u in uni]:
            uni.append(name)
            
    # Вывод исходного списка и списка имен с уникальными длинами
    print(f"{names=}")
    print(f"{uni=}")
