#4. В одну строку вводятся фразы, разделенные символом “;”. 
#Используя генератор списка, создайте список lengths, содержащий количество слов в каждой фразе из исходного списка. 
#Выведите исходный список и список lengths.

if __name__ == "__main__":
    # Считываем строку и разбиваем её на части, используя точку с запятой (;) как разделитель
    phrases = input().split(";")
    
    # Списковое включение для подсчета слов в каждой фразе:
    # 1. phrase.strip() — убираем лишние пробелы по краям фразы
    # 2. .split(" ") — разбиваем очищенную фразу на отдельные слова по пробелу
    # 3. len(...) — считаем количество получившихся слов
    lengths = [len(phrase.strip().split(" ")) for phrase in phrases]
    
    # Выводим список фраз и список соответствующих им количеств слов
    print(f"{phrases=}")
    print(f"{lengths=}")
