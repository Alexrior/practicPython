#5. В строку вводится список цен на товары в магазине. В следующих строках вводятся число K и число M.
#В магазине действует акция: каждые полные К рублей в цене товара дают скидку М рублей.
#Используя один генератор списка, создайте новый список discount, который будет содержать цены после применения скидки.
#Выведите списки цен до скидки и после применения скидки.


if __name__ == "__main__":
    # Считываем цены через пробел, преобразуя каждое значение в число с плавающей точкой (float)
    prices = list(map(float, input().split(" ")))
    
    # K — порог или шаг цены, за который дается скидка
    K = float(input())
    
    # M — фиксированная сумма скидки за каждый пройденный порог K
    M = float(input())

    # Рассчитываем итоговую стоимость для каждой цены:
    # 1. (price // K) — находим, сколько целых раз порог K входит в цену (целочисленное деление)
    # 2. (price // K) * M — вычисляем общую сумму скидки
    # 3. price - ... — вычитаем скидку из исходной цены
    discount = [price - (price // K) * M for price in prices]
    
    # Выводим исходный список цен и список цен после применения скидки
    print(f"{prices=}")
    print(f"{discount=}")
