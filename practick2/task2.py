#2. Email'ы могут состоять из любых букв латинского алфавита верхнего и нижнего регистров, цифр, спецсимволов из набора “+_- #”.
#Выведите email'ы в столбец.

# Компилируем шаблон для поиска email:
# [\d\w+_\-#]+ — имя пользователя: цифры, буквы, символы +, _, -, # (один или более)
# @ — обязательный символ "собачка"
# [\d\w]+ — домен второго уровня: буквы или цифры (минимум одна)
# \. — обязательная точка (экранирована обратным слэшем)
# \w{2,3} — доменная зона: от 2 до 3 букв (например, .ru, .com, .net)
email_pattern = re.compile(r"[\d\w+_\-#]+@[\d\w]+\.\w{2,3}")

if __name__ == "__main__":
    print("Введите текст:")
    # Считываем строку от пользователя
    TEXT = input()
    
    # findall находит все совпадения, которые подходят под шаблон email_pattern
    emails = email_pattern.findall(TEXT)
    
    # Печатаем найденные адреса, разделяя их переносом строки (\n)
    # Оператор * распаковывает список в аргументы функции print
    print(*emails, sep="\n")
